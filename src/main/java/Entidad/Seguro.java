package Entidad;

public class Seguro {
	
	int idSeguro;
	String descripcion;
	int idTipo;
	String costoContratacion;
	String costoAsegurado;
	
	
	public int getIdSeguro() {
		return idSeguro;
	}
	public void setIdSeguro(int idSeguro) {
		this.idSeguro = idSeguro;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getIdTipo() {
		return idTipo;
	}
	public void setIdTipo(int idTipo) {
		this.idTipo = idTipo;
	}
	public String getCostoContratacion() {
		return costoContratacion;
	}
	public void setCostoContratacion(String costoContratacion) {
		this.costoContratacion = costoContratacion;
	}
	public String getCostoAsegurado() {
		return costoAsegurado;
	}
	public void setCostoAsegurado(String costoAsegurado) {
		this.costoAsegurado = costoAsegurado;
	}
	
	
}
